-- If-else Statements
local x = 5
if x > 10 then
    print("Greater then 10")
elseif x == 10 then
    print("Equal to 10")
else 
    print("Less then 10")
end


-- Loops
for i = 1, 5 do
    print("for loop iteration: ", i)
end

local count = 0
while count < 3 do
    print("while loop iteratin: ", count)
    count = count + 1
end